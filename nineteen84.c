void scene2_run(void)
{
    cls();
    cls();

    pcenter(1, s2_hdr, DKGRAY);
    hline(2, 45, DKGRAY);
    pat(10, 1, s2_l1, DKGRAY);
    pat(12, 1, s2_l2, DKGRAY);
    pat(14, 1, s2_l3, DKGRAY);
    hline(16, 45, DKGRAY);

    scroller_init(&scr[0], 10, 6, 0, wave_red, smsg1);
    scroller_init(&scr[1], 12, 6, 1, wave_blue, smsg2);
    scroller_init(&scr[2], 14, 10, 0, wave_gray, smsg3);

    blink_reset();

    char timer = 0;
    char words = 0;
    char col1 = 2;
    char col2 = 2;
    char hold_after_words = 0;

    for (;;)
    {
        wait_frame();
        scroller_step(&scr[0]);
        scroller_step(&scr[1]);
        scroller_step(&scr[2]);
        scroller_crossflash();
        timer++;

        if (words < 4 && timer >= (char)(words * 100 + 80))
        {
            if (words == 0)
            {
                col1 = reveal_word(18, col1, sw_war, RED);
                col2 = reveal_word(20, col2, sw_surveillance, LTRED);
            }
            else if (words == 1)
            {
                col1 = reveal_word(18, col1, sw_dot, DKGRAY);
                col1 = reveal_word(18, col1, sw_freedom, CYAN);
                col2 = reveal_word(20, col2, sw_dot, DKGRAY);
                col2 = reveal_word(20, col2, sw_data, LTBLUE);
            }
            else if (words == 2)
            {
                col1 = reveal_word(18, col1, sw_dot, DKGRAY);
                col1 = reveal_word(18, col1, sw_strength, LTGREEN);
                col2 = reveal_word(20, col2, sw_dot, DKGRAY);
                col2 = reveal_word(20, col2, sw_ai, YELLOW);
            }
            else if (words == 3)
            {
                col1 = reveal_word(18, col1, sw_dot, DKGRAY);
                col1 = reveal_word(18, col1, sw_watching, ORANGE);
                col2 = reveal_word(20, col2, sw_dot, DKGRAY);
                col2 = reveal_word(20, col2, sw_listening, PURPLE);
            }
            words++;
        }

        if (words >= 4)
        {
            hold_after_words++;
            if (hold_after_words >= 150)
                break;
        }
    }

    wait_frames(5);
    vic.color_border = BLACK;
    vic.color_back = BLACK;
    glitch_transition();
    wait_frames(10);
}

