#define KBUF_COUNT (*((volatile char *)0x00C6))
#define KBUF_START ((volatile char *)0x0277)

char read_key(void)
{
    if (!KBUF_COUNT)
        return 0;
    char k = KBUF_START[0];
    char cnt = KBUF_COUNT - 1;
    for (char i = 0; i < cnt; i++)
        KBUF_START[i] = KBUF_START[i + 1];
    KBUF_COUNT = cnt;
    return k;
}

char petscii_to_sc(char p)
{
    if (p >= 65 && p <= 90)
        return p - 64;
    if (p == 32 || p == 46 || p == 33 || p == 63 || p == 45)
        return p;
    if (p >= 48 && p <= 57)
        return p;
    return 0;
}

char input_line(char row, char col, char maxlen, char *buf, char fg)
{
    char len = 0, cur_tick = 0, cur_on = 1;
    for (char i = 0; i < maxlen + 1; i++)
        pch(row, col + i, 32, fg);
    for (;;)
    {
        wait_frame();
        if (++cur_tick >= 20)
        {
            cur_tick = 0;
            cur_on = !cur_on;
        }
        pch(row, col + len, cur_on ? 160 : 32, fg);
        char k = read_key();
        if (!k)
            continue;
        if (k == 13)
            break;
        if (k == 20)
        {
            if (len > 0)
            {
                pch(row, col + len, 32, fg);
                len--;
                pch(row, col + len, 32, fg);
                buf[len] = 0;
            }
            continue;
        }
        char sc = petscii_to_sc(k);
        if (sc && len < maxlen)
        {
            buf[len] = sc;
            pch(row, col + len, sc, fg);
            len++;
            pch(row, col + len, 32, fg);
        }
    }
    pch(row, col + len, 32, fg);
    buf[len] = 0;
    return len;
}

void typeout(char row, char col, const char *s, char fg, char speed)
{
    while (*s)
    {
        pch(row, col, *s, fg);
        s++;
        col++;
        wait_frames(speed);
        if (any_key())
            return;
    }
}

