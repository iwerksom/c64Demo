static unsigned scroll_idx = 0;
static char scroll_buf[40];
static char scroll_tick = 0;

void scroll_init(void)
{
    scroll_idx = 0;
    scroll_tick = 0;
    for (char i = 0; i < 40; i++)
        scroll_buf[i] = 32;
}

void scroll_step(void)
{
    if (++scroll_tick < SCROLL_SPEED)
        return;
    scroll_tick = 0;
    for (char i = 0; i < 39; i++)
        scroll_buf[i] = scroll_buf[i + 1];
    char c = scroll_text[scroll_idx++];
    if (c == 0xFF)
    {
        scroll_idx = 0;
        c = scroll_text[0];
    }
    scroll_buf[39] = c;
    int base = 24 * COLS;
    for (char i = 0; i < 40; i++)
    {
        SCREEN[base + i] = scroll_buf[i];
        COLORRAM[base + i] = LTGREEN;
    }
}

