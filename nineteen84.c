void sprite_load(const char *data)
{
    char *dst = SPRITE_MEM;
    for (char i = 0; i < 63; i++)
        dst[i] = data[i];
    dst[63] = 0;
}

void sprite_show(char x, char y, char color)
{
    SPR_PTR0 = SPRITE_BLOCK;
    VIC_SPR0_X = x;
    VIC_SPR0_Y = y;
    VIC_SPR_MSBX &= ~1;
    VIC_SPR0_COLOR = color;
    VIC_SPR_MCOLOR &= ~1;
    VIC_SPR_PRIOR &= ~1;
    VIC_SPR_EXPX |= 1;
    VIC_SPR_EXPY |= 1;
    VIC_SPR_ENA |= 1;
}

void sprite_hide(void)
{
    VIC_SPR_ENA &= ~1;
    VIC_SPR_EXPX &= ~1;
    VIC_SPR_EXPY &= ~1;
}

